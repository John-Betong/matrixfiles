or diesel) and it has nothing whatsoever to do with solar 
energy, wind energy or inverters.<br>  
»&nbsp;It preserves the safety of our immediate environment from 
noise and air pollution because it is noiseless and smokeless. 
Since it does not use fuel at all - there is no smoke which is 
usually caused by carbon combustion in fuel.<br> 
»&nbsp;The components needed to fabricate it are readily and easily 
available and can be sourced with ease.<br>  
»&nbsp;It can be built to any capacity, depending on the capacity of the 
load we want it to carry.<br> 
»&nbsp;It does not require any mechanical service or maintenance.<br>  
