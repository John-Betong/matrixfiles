»&nbsp;It saves a lot of money individual and business owners spend 
on fuel.<br> 
»&nbsp;It is very safe to use, it does not wear or tear and it can work 
non-stop for as long as you want.<br>
</p>

<h4 style="text-align: center; padding:0px 0px 10px;">"The greatest invention of the age"</h4>

<p>On February 28, 1928, a man called Lester Hendershot made front-page headlines across the nation with his invention of a "fuel less 
motor".  Hendershot managed to develop power with his device by cutting the earth's magnetic field as our normal generator cuts its own magnetic field.</p>

<img src="hendershot/generator.jpg" class="plate" alt="A hendershot generator"><br>

<p>Not claiming his device to be a perpetuum mobile, Hendershot explained that it was tapping the earth's magnetic field and rotation as its energy source. The Hendershot Device concerns a self-running oscillator.</p>

<p>There are a pair of large air-core coils positioned in a "basket weave" pattern, cylindrical capacitors inside the coils, several high-value capacitors, a couple of standard transformers and a permanent-magnet "buzzer" for a regulator. The two large coils are tuned to resonate with each other.</p>

<p>Lester Hendershot lived in a small house near the railway line in Elizabeth, Pennsylvania. He had not been many studies, but he took courses in mechanics at Cornell University. </p>

<p>Hendershot's machine was not actually a motor, but a generator. It developed electricity which could power another motor but did not produce any usable motion itself. The idea for the generator first came to him in a dream in his early twenties. He forgot the idea for several years and was motivated to start working on it to replace the broken motor in his child's toy airplane.</p>

